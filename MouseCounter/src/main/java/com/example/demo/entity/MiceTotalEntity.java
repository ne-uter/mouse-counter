package com.example.demo.entity;

public class MiceTotalEntity {

	private String size;
	private int males_total;
	private int females_total;
	
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public int getMales_total() {
		return males_total;
	}
	public void setMales_total(int males_total) {
		this.males_total = males_total;
	}
	public int getFemales_total() {
		return females_total;
	}
	public void setFemales_total(int females_total) {
		this.females_total = females_total;
	}
}
